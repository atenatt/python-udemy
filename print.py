""" Aula 18 - Funcoes do print """

# Print normal de string
print ("Victor")

# Print em lista
print(12, 56)

# Print com separador
print(10, 20, sep="-")

# Quebra de linha
# \n -> LF
# \r \n -> CRLF
print("Victor", end='\n')
print("Pedro", end='\n')

# Sem quebra de linha
print("Victor", end=' ')
print("Pedro", end='')